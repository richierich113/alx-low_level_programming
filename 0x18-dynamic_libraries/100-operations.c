/**
 * add - adds x and y values
 * @x: integer parameter
 * @y: integer parameter
 * Return: sum of x and y
 */

int add(int x, int y)
{
	int sum;

	sum = x + y;
	return (sum);
}

/**
 * sub - subtracts x from y
 * @x: integer parameter
 * @y: integer parameter
 * Return: difference
 */

int sub(int x, int y)
{
	int diff;

	diff = x - y;
	return (diff);
}

/**
 * mul - multiply x and y
 * @x: integer parameter
 * @y: integer parameter
 * Return: product of x and y
 */

int mul(int x, int y)
{
	int product;

	product = x * y;
	return (product);
}
/**
 * div - divide x by y
 * @x: integer parameter
 * @y: integer parameter
 * Return: quotient
 */

int div(int x, int y)
{
	int quotient;

	quotient = x / y;
	return (quotient);
}
/**
 * mod - mods x by y
 * @x: integer parameter
 * @y: integer parameter
 * Return: modulus
 */

int mod(int x, int y)
{
	int modulus;

	modulus = x % y;
	return (modulus);
}
